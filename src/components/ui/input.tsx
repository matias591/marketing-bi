import * as React from "react";
import { cn } from "@/lib/utils";

const Input = React.forwardRef<HTMLInputElement, React.ComponentProps<"input">>(
  ({ className, type, ...props }, ref) => {
    return (
      <input
        type={type}
        className={cn(
          "flex h-9 w-full rounded-md border bg-(--color-surface) px-3 py-1 text-sm shadow-sm transition-colors",
          "placeholder:text-(--color-text-muted) focus-visible:outline-hidden focus-visible:ring-2 focus-visible:ring-(--color-accent)/40",
          "disabled:cursor-not-allowed disabled:opacity-50",
          className,
        )}
        ref={ref}
        {...props}
      />
    );
  },
);
